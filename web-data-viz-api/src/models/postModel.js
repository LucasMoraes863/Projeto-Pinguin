var database = require("../database/config");

function listar() {
    var instrucaoSql = `
        SELECT 
            p.id AS idPost,
            p.titulo,
            p.conteudo,
            p.autor_id,
            p.criado_em,
            u.id AS idUsuario,
            u.nome
        FROM post p
            JOIN usuario u
                ON p.autor_id = u.id;
    `;
    console.log("Executando a instrução SQL: \n" + instrucaoSql);
    return database.executar(instrucaoSql);
}

